let markdown = fs.readFileSync(coursePath, "utf8");
const inserted = [];

for (const [code, text] of Object.entries(DEEP_DIVES)) {
  const startMatch = markdown.match(new RegExp(`^###\\s+${code}[：:]`, "m"));
  if (!startMatch) {
    throw new Error(`Missing lesson block ${code}`);
  }
  const start = startMatch.index;
  const nextMatch = markdown.slice(start).match(/\r?\n---\s*\r?\n/);
  if (!nextMatch) {
    throw new Error(`Missing lesson block terminator ${code}`);
  }
  const end = start + nextMatch.index;
  const block = markdown.slice(start, end);

  if (/^####\s+deepDives\s*$/m.test(block)) {
    continue;
  }
  if (!/^####\s+lessonManuals\s*$/m.test(block)) {
    throw new Error(`Could not locate lessonManuals block for ${code}`);
  }

  const replacement = `${block.trimEnd()}\n\n#### deepDives\n\`\`\`\n${text}\n\`\`\`\n\n`;
  markdown = `${markdown.slice(0, start)}${replacement}${markdown.slice(end)}`;
  inserted.push(code);
}

fs.writeFileSync(coursePath, markdown, "utf8");

