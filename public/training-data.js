function makeWeeks() {
  return WEEK_BLUEPRINTS.map(([id, phaseId, week, title, lessonTitles], index) => ({
    id,
    phaseId,
    week,
    title,
    objective: `${title}要求队员能说清场景、做出动作、留下记录，并把错误转化为下一次补训。`,
    standard: index < 6 ? "全员完成基础动作和口令确认。" : index < 14 ? "动作、报告、队形和AAR均可被观察员记录。" : "能在对抗压力下保持SOP和指挥链不断裂。",
    warning: PHASE_BRIEFS[phaseId],
    lessons: lessonTitles.map((lessonTitle, lessonIndex) => ({
      id: `${id}-l${lessonIndex + 1}`,
      code: `${week}-L${lessonIndex + 1}`,
      title: lessonTitle.replace(/^L\d+\s/, ""),
      goal: lessonTitle,
      actionStandard: lessonIndex === 0 ? "先确认概念和适用边界，再进入动作。" : lessonIndex === 1 ? "在低压力下完成标准动作，再加入时间和通讯约束。" : "完成一次可记录、可复盘、可复测的闭环。",
      commonError: lessonIndex === 0 ? "只背术语，不知道什么时候不用。" : lessonIndex === 1 ? "动作完成了，但没有报告、方向或队友位置意识。" : "复盘只谈输赢，不留下下一次训练动作。",
      drill: lessonIndex === 0 ? "教官演示后由队员复述适用场景。" : lessonIndex === 1 ? "两人或三人组完成3轮标准动作。" : "观察员记录事实，班长给出补训动作。"
    }))
  }));
}

